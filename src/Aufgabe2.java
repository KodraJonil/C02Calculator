public class Aufgabe2 {

    public static void main(String[] args) {
        int a = 5;
        int b = 2;

        System.out.println(a + " + " + b + " = " + (a+b));
        System.out.println(a + " - " + b + " = " + (a-b));
        System.out.println(a + " * " + b + " = " + (a/b));
        System.out.println(a + " mod " + b + " = " + (a%b));
    }
}
